public class PrintShopException extends Exception
{
    private static final long serialVersionUID = 1L;

    public PrintShopException() {
    }

    public PrintShopException(String arg0) {
        super(arg0);
    }


    public PrintShopException(Throwable arg0) {
        super(arg0);
    }

    public PrintShopException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
}
